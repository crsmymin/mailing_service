package com.cside.new_mailing.VO;

public class ContentsVO {
	private String contents_id;
	private String contents_name;
	private String contents_send_yn;
	private String req_date;
	private String delete_date;
	private String contents_html;
	
	public String getContents_id() {
		return contents_id;
	}
	public void setContents_id(String contents_id) {
		this.contents_id = contents_id;
	}
	public String getContents_name() {
		return contents_name;
	}
	public void setContents_name(String contents_name) {
		this.contents_name = contents_name;
	}
	public String getContents_send_yn() {
		return contents_send_yn;
	}
	public void setContents_send_yn(String contents_send_yn) {
		this.contents_send_yn = contents_send_yn;
	}
	public String getReq_date() {
		return req_date;
	}
	public void setReq_date(String req_date) {
		this.req_date = req_date;
	}
	public String getDelete_date() {
		return delete_date;
	}
	public void setDelete_date(String delete_date) {
		this.delete_date = delete_date;
	}
	public String getContents_html() {
		return contents_html;
	}
	public void setContents_html(String contents_html) {
		this.contents_html = contents_html;
	}
	
	
	
}

